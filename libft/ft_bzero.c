#include "libft.h"

void		ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*stemp;

	i = 0;
	stemp = s;
	if (n == 0)
		return ;
	while (i < n)
	{
		stemp[i] = '\0';
		i++;
	}
}
