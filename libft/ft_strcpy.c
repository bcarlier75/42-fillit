#include "libft.h"

char		*ft_strcpy(char *dst, const char *src)
{
	int		c;

	c = 0;
	while (src[c] != '\0')
	{
		dst[c] = src[c];
		c++;
	}
	dst[c] = '\0';
	return (dst);
}
